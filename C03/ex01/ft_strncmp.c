int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				diff;

	i = 0;
	if (n == 0)
		return (0);
	while ((*s2 != 0 || *s1 != 0) && *s1 == *s2 && i < n - 1)
	{
		s1++;
		s2++;
		i++;
	}
	diff = *s1 - *s2;
	return (diff);
}
