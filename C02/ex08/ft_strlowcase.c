char	*ft_strlowcase(char *str)
{
	char *ori;

	ori = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str = *str + 32;
		}
		str++;
	}
	return (ori);
}
