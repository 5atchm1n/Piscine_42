int	ft_iterative_factorial(int nb)
{
	int n;
	int f;

	n = nb;
	f = 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	while (nb / n != nb)
	{
		f = f * n;
		n--;
	}
	return (f);
}
